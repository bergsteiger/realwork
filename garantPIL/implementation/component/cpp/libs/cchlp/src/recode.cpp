char* Recoding ( code_t in, code_t out, char* dest, const char* src )
{
	unsigned char* curTable;	
	int i = 0;
	if ( dest ) {
		if ( !src ) src = dest;
		curTable = codeTables[in][out];
		if ( curTable ) {
			i = 0;
			while (src[i]) {
				dest[i] = curTable[(unsigned char)src[i]]; 
				i++;
			}
			dest [i] = 0;
		} else if ( dest != src ) strcpy ( dest, src );
	}
	return dest;
}
