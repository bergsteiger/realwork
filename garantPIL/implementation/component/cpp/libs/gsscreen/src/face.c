static void put_perc(short row, short col, short percent)
{
	char str_perc[5];

	if( percent >= 100 ) {
		str_perc[0] = '1';
		str_perc[1] = '0';
		str_perc[2] = '0';
	}
	else {
		str_perc[0] = ' ';
		if( percent < 10 ) {
			str_perc[1] = ' ';
			str_perc[2] = percent+'0';
		}
		else {
			str_perc[1] = percent/10 + '0';
			str_perc[2] = percent%10 + '0';
		}
	}
	str_perc[3] = '%';
	str_perc[4] = '\0';

	write_str(row, col, str_perc, FS_WINDOW);
}

